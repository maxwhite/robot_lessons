package com.ua.java.lesson18;

public class Student {

    int age;

    String course;

    public Student() {

    }

    public void setAge(int age) {
        this.age = age;
    }

    public void setCourse(String course) {
        this.course = course;
    }

    public int getAge() {
        return age;
    }

    public String getCourse() {
        return course;
    }
}
